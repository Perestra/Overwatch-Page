// Active mode for Left menu mobile
const mainNav = document.querySelector('.full_nav')

function addMenuMainNav() {
    mainNav.classList.add('active'); //Add an 'Active' class for open the menu
}
function RemoveMenuMainNav() {
    mainNav.classList.remove('active'); //Remove an 'Active' class for open the menu
}

